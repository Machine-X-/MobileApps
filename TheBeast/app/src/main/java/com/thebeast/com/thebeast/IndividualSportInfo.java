public class IndividualSportInfo {

    private int wins;
    private int losses;
    private int stat1;
    private int stat2;

    public IndividualSportInfo() {
        this.wins = 0;
        this.losses = 0;
        this.stat1 = 0;
        this.stat2 = 0;
    }

    public int getWins() {
        return wins;
    }

    public int getLosses() {
        return losses;
    }

    public int getStat1() {
        return stat1;
    }

    public int getStat2() {
        return stat2;
    }
}
